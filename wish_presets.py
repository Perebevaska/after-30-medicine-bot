# code → text (плоский справочник для валидации/резолва).
_BY_CODE: dict[str, str] = {
    p["code"]: p["text"] for band in _BANDS.values() for p in band
}


def band_for_hour(hour: int) -> str:
    """Возвращает временной диапазон по часу (0–23) в локальной TZ юзера."""
    if 5 <= hour < 11:
        return "morning"
    if 11 <= hour < 17:
        return "day"
    if 17 <= hour < 23:
        return "evening"
    return "night"


def presets_for_hour(hour: int) -> list[dict[str, str]]:
    """Пресеты, подходящие по времени суток. Копия (изоляция от мутаций)."""
    return [dict(p) for p in _BANDS[band_for_hour(hour)]]


def is_valid_code(code: str) -> bool:
    """True, если код есть в каталоге (антифрод: шлём только пресеты)."""
    return code in _BY_CODE


def text_for_code(code: str) -> str | None:
    """Текст пресета по коду, либо None если код неизвестен."""
    return _BY_CODE.get(code)
